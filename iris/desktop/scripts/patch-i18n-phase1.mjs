// Deep merge `patch` into `base` in place. Plain objects are merged
// recursively; arrays and primitives are replaced. New keys are appended
// in iteration order, so they show up at the end of their parent object.
function deepMerge(base, patch) {
  for (const [key, value] of Object.entries(patch)) {
    if (
      value !== null &&
      typeof value === 'object' &&
      !Array.isArray(value) &&
      base[key] !== null &&
      typeof base[key] === 'object' &&
      !Array.isArray(base[key])
    ) {
      deepMerge(base[key], value);
    } else {
      base[key] = value;
    }
  }
  return base;
}

async function patchFile(locale, namespace, patch) {
  const filePath = join(LOCALES_DIR, locale, `${namespace}.json`);
  const raw = await readFile(filePath, 'utf8');
  const json = JSON.parse(raw);
  deepMerge(json, patch);
  await writeFile(filePath, JSON.stringify(json, null, 2) + '\n', 'utf8');
  return filePath;
}

async function main() {
  const results = [];
  for (const [locale, namespaces] of Object.entries(PATCHES)) {
    for (const [namespace, patch] of Object.entries(namespaces)) {
      try {
        const filePath = await patchFile(locale, namespace, patch);
        results.push(`  ok  ${locale}/${namespace}.json`);
      } catch (err) {
        results.push(`  ERR ${locale}/${namespace}.json — ${err.message}`);
      }
    }
  }
  console.log(results.join('\n'));
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
